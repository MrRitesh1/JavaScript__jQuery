function maltipleceshan(p1, p2) {
  return p1 * p2;
}
console.log(maltipleceshan(44, 65));

function sum(a1, a2) {
  return a1 + a2;
}
console.log(sum(76, 9));

function maltipal(b1, b2, b3) {
  return b1 + b2 - b3;
}
console.log(maltipal(16, 54, 23));

function maltipalopreshan(c1, c2, c3, c4) {
  return c1 * c2 - c3 + c4;
}
console.log(maltipalopreshan(4, 2, 2, 2));
